import { Song, SongNote } from '../types';

type N = [string, number, number];

function ms(id: string, title: string, artist: string, d: Song['difficulty'], bpm: number, ns: N[]): Song {
  const spb = 60 / bpm;
  const notes: SongNote[] = ns.map(([note, beat, dur]) => ({
    note, time: +(beat * spb).toFixed(3), duration: +(dur * spb).toFixed(3)
  }));
  const last = ns[ns.length - 1];
  return { id, title, artist, difficulty: d, bpm, notes, duration: (last[1] + last[2]) * spb + 1 };
}

